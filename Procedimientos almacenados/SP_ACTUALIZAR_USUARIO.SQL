create or replace PROCEDURE SP_ACTUALIZAR_USUARIO(
  
IN_ID_USUARIO 	IN NUMBER,
IN_CONTRASENA IN VARCHAR2,
IN_CORREO IN VARCHAR2,
IN_ROL_ID IN NUMBER,
IN_HABILITADO IN NUMBER,
OUT_GLOSA 	OUT VARCHAR2,
OUT_ESTADO 	OUT NUMBER
) AS


BEGIN

    OUT_GLOSA := 'SP_ACTUALIZAR_USUARIO ejecutado exitosamente';
    OUT_ESTADO := 0;
 
   UPDATE FERIA2.USUARIO SET CONTRASENA=IN_CONTRASENA, CORREO=IN_CORREO, ROL_ID=IN_ROL_ID, habilitado=IN_HABILITADO WHERE ID=IN_ID_USUARIO ;
   
    EXCEPTION
        WHEN OTHERS THEN
            OUT_ESTADO := -1;
            OUT_GLOSA := feria2.FN_GET_GLOSA_ERROR;
END SP_ACTUALIZAR_USUARIO;